const fs = require('fs');
const path = require('path');

// Create a basic PNG file header (1x1 transparent pixel)
const pngHeader = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D, 
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00, 
  0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00, 
  0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49, 
  0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

// Create directory if it doesn't exist
const iconDir = path.join(__dirname, 'media', 'icons');
if (!fs.existsSync(iconDir)) {
  fs.mkdirSync(iconDir, { recursive: true });
}

// Write the PNG file
const iconPath = path.join(iconDir, 'ape-icon.png');
fs.writeFileSync(iconPath, pngHeader);

console.log(`Created PNG icon at: ${iconPath}`);